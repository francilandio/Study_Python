# 4 - Faça um programa que peça as 4 notas bimestrais e mostre a média.

nota_1 = float(input('Informe a nota do 1º bimestre: '))
nota_2 = float(input('Informe a nota do 1º bimestre: '))
nota_3 = float(input('Informe a nota do 1º bimestre: '))
nota_4 = float(input('Informe a nota do 1º bimestre: '))

media = (nota_1 + nota_2 + nota_3 + nota_4) / 4

print(f'Sua média foi: {media:.2f}')
